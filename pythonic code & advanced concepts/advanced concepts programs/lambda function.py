square = lambda x: x*x
#lambda function is a small anonymous function. 
# It can take any number of arguments, but can only have one expression. 
# The expression is evaluated and returned. 
# Lambda functions are used when we need a small function for a short period of time. 
# They are often used as arguments to higher-order functions, such as map(), filter(), 
# and reduce().
print(square(5))